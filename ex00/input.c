#include <stdlib.h>

void	input(char **argv, int **nb)
{
	int i;
	int j;

	i = 1;
	while (i <= 9)
	{
		j = 0;
		while (j < 9)
		{
			if (argv[i][j] != '.')
				nb[i - 1][j] = argv[i][j] - '0';
			else
				nb[i - 1][j] = 0;
			j++;
		}
		i++;
	}
}
